/*
[표현식과 연산자]

1. 표현식 
: 어떠한 결과 값으로 평가되는 식.

2. 할당 연산자
: 오른쪽 표현식을 왼쪽 피연산자 값에 할당하는 연산자

3. 비교 연산자
: 좌측 피연산자, 우측 피연산자 비교하는.
- true, false 반환

4. 산술 연산자
: 덧셈, 뺄셈, 곱셈, 나눗셈을 하는.
- Number 반환.

5. 비트 연산자
: 비트를 직접 조작.

6. 논리 연산자
: Boolean을 통해 참과 거짓을 검증하는.

7. 삼항 연산자
: 조건에 따라 값을 선택하는 연산자.

조건 ? 참 : 거짓

8. 관계 연산자
: 객체에 속성이 있는지 확인하기 위한 연산자.

9. typeof
: 피연산자의 타입을 반환하는 연산자
- 문자열로 반환.

*/